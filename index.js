// 1. 두 수의 차

function solution(num1, num2) {
    return num1 - num2;
}

// 2. 두 수의 곱

function solution(num1, num2) {
    return num1 * num2;
}

// 3. 두 수의 몫

function solution(num1, num2) {
    return parseInt(num1 / num2);
}

// 4. 나이 구하기

function solution(age) {
    return (2023 - age);
}

// 5. 숫자 비교하기

function solution(num1, num2) {
    let result = (num1 === num2) ? 1 : -1;
    return result;
}

// 6. 두 수의 합

function solution(num1, num2) {
    return num1 + num2;
}
